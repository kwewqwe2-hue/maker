def search_bridges(keyword):
    keyword_lower = keyword.lower()
    results = []
    all_items = []
    for b in BRIDGE_TIMELINE:
        all_items.append(b)
    for t_name, t_info in BRIDGE_TYPES.items():
        all_items.append({u"name": t_name, **t_info, u"type": u"桥型"})
    for period, p_info in BRIDGE_DATA.items():
        all_items.append({u"name": period, **p_info, u"type": u"时期"})

    for item in all_items:
        name = str(item.get(u"name", u""))
        desc = str(item.get(u"desc", u""))
        loc = str(item.get(u"location", u""))
        btype = str(item.get(u"type", u""))
        if keyword_lower in name.lower() or keyword_lower in desc.lower() or keyword_lower in loc.lower() or keyword_lower in btype.lower():
            results.append(item)
    return results

