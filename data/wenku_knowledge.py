def get_term(term_name):
    """获取术语词条"""
    return WENKU_TERMS.get(term_name)

def get_architecture(name):
    """获取建筑词条"""
    for category, buildings in ARCHITECTURE_TYPES.items():
        if name in buildings:
            return buildings[name]
    return None

def get_bridge(name):
    """获取桥梁词条"""
    return BRIDGE_ARCHITECTURE.get(name)

def search_all(keyword):
    """搜索所有词条"""
    results = []
    keyword_lower = keyword.lower()

    for name, info in WENKU_TERMS.items():
        if keyword_lower in name.lower() or keyword_lower in info.get("content", "").lower():
            results.append({
                "type": "term",
                "category": info.get("category", ""),
                "name": name,
                "title": info.get("title", name),
                "content": info.get("content", "")[:200]
            })

    for category, buildings in ARCHITECTURE_TYPES.items():
        for name, info in buildings.items():
            if keyword_lower in name.lower() or keyword_lower in info.get("content", "").lower():
                results.append({
                    "type": "architecture",
                    "category": info.get("category", category),
                    "name": name,
                    "title": info.get("title", name),
                    "content": info.get("content", "")[:200]
                })

    for name, info in BRIDGE_ARCHITECTURE.items():
        if keyword_lower in name.lower() or keyword_lower in info.get("content", "").lower():
            results.append({
                "type": "bridge",
                "category": info.get("category", "桥梁"),
                "name": name,
                "title": info.get("title", name),
                "content": info.get("content", "")[:200]
            })

    return results

def get_all_terms():
    """获取所有术语"""
    return WENKU_TERMS

def get_all_architectures():
    """获取所有建筑"""
    return ARCHITECTURE_TYPES

def get_all_bridges():
    """获取所有桥梁"""
    return BRIDGE_ARCHITECTURE
